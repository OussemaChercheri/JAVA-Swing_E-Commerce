    private boolean isEmpty(){
        if (jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nom est obligatoire","Alert",2);
            return false;
        }if(!jTextField1.getText().matches("[a-zA-Z ]+")){
            JOptionPane.showMessageDialog(this, "Nom est invalide","Alert",2);
            return false;
        }
        if (jPasswordField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Mot de passe est obligatoire","Alert",2);
            return false;
        }
        return true;
    }
    
