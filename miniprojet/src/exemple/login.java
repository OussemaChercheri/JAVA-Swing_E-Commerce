    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                       
    String nom = jTextField1.getText();
    String passe = jPasswordField1.getText();
    String role = txtrole.getSelectedItem().toString();

    if (isEmpty()) {
        try {
            // Create a connection to the database
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/e_commerce", "root", "");

            // Create a prepared statement to execute the SQL query
            PreparedStatement pst = con.prepareStatement("SELECT * FROM user WHERE nom=? AND passe=? AND role=?");
            pst.setString(1, nom);
            pst.setString(2, passe);
            pst.setString(3, role);

            // Execute the query and get the result set
            ResultSet rs = pst.executeQuery();

            // Check if the user exists in the database
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Login successful");

                // Perform the CRUD operations here
                if (role.equals("Client")) {
                    MenuClient c = new MenuClient();
                    c.setVisible(true);
                } else if (role.equals("Vendeur")) {
                    MenuVendeur v = new MenuVendeur();
                    v.setVisible(true);
                }

                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Invalid username or password");
            }

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }



    }//GEN-LAST:event_jButton1ActionPerformed

